#ifndef LIBCALC_HPP
#define LIBCALC_HPP

int adicao(int n1, int n2)
{
    return n1 + n2;
}

int subtracao(int n1, int n2)
{
    return n1 - n2;
}

int multiplicacao(int n1, int n2)
{
    return n1 * n2;
}

float divisao(float n1, float n2)
{
    return n1 / n2;
}

#endif // LIBCALC_HPP
